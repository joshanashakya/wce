public class DataItemValue {

    private DataItem from;

    private DataItem to;

    private double minValue;

    public DataItem getFrom() {
        return from;
    }

    public void setFrom(DataItem from) {
        this.from = from;
    }

    public DataItem getTo() {
        return to;
    }

    public void setTo(DataItem to) {
        this.to = to;
    }

    public double getMinValue() {
        return minValue;
    }

    public void setMinValue(double minValue) {
        this.minValue = minValue;
    }
}
